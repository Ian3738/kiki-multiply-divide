// 洗牌（會 mutate）
export function shuffleInPlace<T>(arr: T[]): T[] {
  for (let i = arr.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [arr[i], arr[j]] = [arr[j], arr[i]];
  }
  return arr;
}

// 取 n 題（不夠就洗多輪補滿）
export function pickQuestions(n: number, shuffle = true): Question[] {
  let pool: Question[] = QUESTIONS.slice();
  if (shuffle) shuffleInPlace(pool);
  if (n <= pool.length) return pool.slice(0, n);
  const out: Question[] = pool.slice();
  while (out.length < n) {
    const more = QUESTIONS.slice();
    if (shuffle) shuffleInPlace(more);
    out.push(...more);
  }
  return out.slice(0, n);
}